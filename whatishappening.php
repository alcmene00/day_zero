            <h3 class="titles" id="Cape-Town">Cape Town - South Africa</h3>
            <p>
              <br />
              The Cape Town water crisis in South Africa was a period of severe
              water shortage in the Western Cape region, most notably affecting
              the City of Cape Town. While dam water levels had been declining
              since 2015, the Cape Town water crisis peaked during mid-2017 to
              mid-2018 when water levels hovered between 15 and 30 percent of
              total dam capacity. <br />
              <br />
              The City of Cape Town introduced increasingly strict restrictions,
              which as well as limiting the volumes allowed, also restricted
              what the water was used for. Filling swimming pools, washing cars,
              and fountains were all banned. Households using high volumes of
              water faced big fines.
