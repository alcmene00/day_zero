            <!-- YEMEN -->
            
            <h3 class="titles" id="Yemen">Yemen - Western Asia</h3>
            <p>
              The Yemeni Civil War has led to tens of thousands of deaths, but
              the international community is just starting to appreciate the
              gravity of a humanitarian crisis that has preoccupied Yemeni
              policymakers for years: water scarcity. As Iranian-backed Houthi
              rebels battle Emirati- and Saudi-trained militias across Yemen,
              the country’s already-strained water-supply networks are running
              dry. <br />
              <br />
              In 2013, two years before the Yemeni Civil War began in earnest,
              some experts predicted that the Yemeni capital of Sana’a would
              lose its water supply within 10 years. Since then, a deadly
              combination of climate change and political violence has
              aggravated water scarcity in the country. <br /><br />
              "Yemen is considered the most water-poor country in the world,"
              claims Abdoul Razaz Saleh, a former Yemeni water and environment
              minister. "It is one of the countries with no rivers, and rainfall
              has decreased significantly over the last three decades. Many
              Yemeni cities and villages suffer from water scarcity—especially
              in the winter. These days, Taiz is practically without water."
            </p>
