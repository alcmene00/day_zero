              "Agriculture—namely crops such as khat, fruit, and
              vegetables—consumes the lion’s share of the available water in
              Yemen, about 90 percent,” says Tawfeeq al-Sharjabi, the Yemeni
              deputy water and environment minister. “For their part, only 55
              percent of Yemeni households have access to safe drinking water,
              and this number shrinks to 35 percent in rural areas. Most of the
              wells drilled for agricultural purposes are illegal, but the
              Yemeni government’s ability to enforce these rules is weak."
