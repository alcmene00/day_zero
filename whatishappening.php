            <!-- CHENNAI -->
            
            <h3 class="titles" id="Chennai">Chennai - India</h3>
            <p>
              Chennai, with a population of nearly 11 million, has no perennial
              rivers to supply its water. The city is dependent on the
              north-east monsoons and four large reservoirs for its water. But
              reliance on these reservoirs is proving increasingly precarious.
              <br />
              <br />
              Two years of deficient monsoon rainfall, particularly in late 2017
              and throughout much of 2018 had led to this water crisis. A major
              heat wave in India from May to June 2019 further exacerbated the
              problem by evaporating any water still left in reservoirs. <br />
              <br />
              In June 2019, Chennai made international headlines when the city
              officials declared that Day Zero had arrived – the anticipated
              moment when all four of the city's reservoirs ran dry. The crisis
              prompted extreme measures to provide relief, including
              transporting water by rail from a town more than 200km (124 miles)
              away. The last train that carried water into the parched city of
              Chennai rolled out of town Oct. 9, almost four months after
              officials declared the city at “day zero”—with no water for a
              population of more than eight million people.
            </p>
